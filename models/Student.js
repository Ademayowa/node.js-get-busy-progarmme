const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// Create schema
const studentSchema = new Schema({
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = Student = mongoose.model('students', studentSchema);
